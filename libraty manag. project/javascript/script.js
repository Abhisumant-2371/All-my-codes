function showmenu() {
    document.getElementById('navbarid-2').style.top="62px";
    document.getElementById('showmenu').classList.add("hideclass");
    document.getElementById('hidemenu').classList.add("showclass");
    
}
function hidemenu() {
    document.getElementById('navbarid-2').style.top="-82px";
    document.getElementById('showmenu').classList.add("showclass");
    document.getElementById('hidemenu').classList.add("hideclass");
    
}
